    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        isCalculated = false;
        try {
            double qty = Double.valueOf(txtQuantity.getText());
            double itmprice = Double.valueOf(txtItem_price.getText());
            
            double sum = qty * itmprice;
            
            itemName.add(txtItem_name.getText());
            quantity.add(txtQuantity.getText());
            itemPrice.add(txtItem_price.getText());
            subtotal.add(String.valueOf(sum));
            totalAmount = totalAmount + sum;
            txtTotal_amount.setText(totalAmount + "");
            clear();
        } catch (Exception ex) {
            
            JOptionPane.showMessageDialog(null, "Valid informations are required to process", "Info" + " ", JOptionPane.INFORMATION_MESSAGE);
        }
        

    }//GEN-LAST:event_btnAddActionPerformed

