    private void readData() {
        
        try {
            file = new File("Data.dat");
            fis = new FileInputStream(file);
            ois = new ObjectInputStream(fis);
            
            boolean isExist = true;
            
            while (isExist) {
                if (fis.available() != 0) {
                    Item itm = (Item) ois.readObject();
                    items.add(itm);
                } else {
                    isExist = false;
                }
            }
            
            for (Item itm : items) {
                
                System.out.println(itm.getName());
                
            }
            
            ois.close();
            fis.close();
            
        } catch (Exception ex) {
            System.out.println("Daily details File not found ");
        }
    }
    

